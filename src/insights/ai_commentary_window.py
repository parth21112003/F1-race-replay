    def on_telemetry_data(self, data: dict):
        """Called every telemetry tick — add new events to the feed."""
        # Track frame data for summary generation
        frame = data.get("frame")
        if frame:
            self._all_frames.append(frame)

        events = data.get("race_events", [])
        if not events:
            return

        # Keep the full event list for summary generation
        self._all_race_events = events

        scroll_bar = self.scroll_area.verticalScrollBar()
        old_scroll_value = scroll_bar.value()
        was_at_bottom = old_scroll_value >= scroll_bar.maximum() - 24

        new_count = 0
        for event in events:
            key = (event.get("type"), round(event.get("time", 0), 1),
                   event.get("driver", ""))
            if key in self._seen_event_keys:
                continue
            self._seen_event_keys.add(key)

            # Remove placeholder on first event
            if self.placeholder.isVisible():
                self.placeholder.hide()

            card = EventCard(event)
            
            self.feed_layout.addWidget(card)
            self._event_widgets.append(card)
            new_count += 1

        if new_count > 0:
            total = len(self._seen_event_keys)
            self.event_count_label.setText(f"{total} events detected")

            if was_at_bottom:
                QTimer.singleShot(0, self._scroll_to_latest_event)
            else:
                QTimer.singleShot(0, lambda value=old_scroll_value: scroll_bar.setValue(value))

    def _scroll_to_latest_event(self):
        """Keep live-follow users on the latest event without landing in empty space."""
        if self._event_widgets:
            self.scroll_area.ensureWidgetVisible(self._event_widgets[-1], 0, 12)


    # ── Summary generation ───────────────────────────────────────────────

    def _on_generate_summary(self):
        """Handle the Generate Summary button click."""
        if self._is_generating_summary:
            return

        if not self._all_race_events:
            self.summary_text.setPlainText(
                "No race events detected yet. Wait for events to appear "
                "in the feed before generating a summary."
            )
            self.summary_panel.setVisible(True)
            return

        settings = get_settings()
        api_key = settings.groq_api_key
        if not api_key:
            self.summary_text.setPlainText(
                "⚠️ No Groq API key configured. Go to Settings to add your key."
            )
            self.summary_panel.setVisible(True)
            return

        # Show panel with loading state
        self._is_generating_summary = True
        self.summary_btn.setEnabled(False)
        self.summary_btn.setText("⏳ Generating…")
        self.summary_text.setPlainText("Generating race summary — this may take a few seconds…")
        self.summary_panel.setVisible(True)

        # Run in background thread
        events_copy = list(self._all_race_events)
        # Use only a sample of frames (first, last, and every 100th)
        frames_sample = []
        if self._all_frames:
            frames_sample.append(self._all_frames[0])
            for i in range(100, len(self._all_frames), 100):
                frames_sample.append(self._all_frames[i])
            frames_sample.append(self._all_frames[-1])

        thread = threading.Thread(
            target=self._generate_summary_thread,
            args=(events_copy, frames_sample, api_key, settings.ai_model),
            daemon=True,
        )
        thread.start()

    def _generate_summary_thread(self, events, frames, api_key, model):
        """Background thread for summary generation."""
        try:
            from src.insights.ai_commentary_engine import AICommentaryEngine
            engine = AICommentaryEngine(
                api_key=api_key,
                model=model or "llama-3.3-70b-versatile"
            )
            summary = engine.generate_race_summary(events, frames)
            if summary:
                self._summary_ready.emit(summary)
            else:
                self._summary_error.emit(
                    "Failed to generate summary. Check your API key and try again."
                )
        except Exception as e:
            msg = str(e)
            if "429" in msg or "RESOURCE_EXHAUSTED" in msg:
                import re
                if "PerDay" in msg or "limit: 20" in msg or "limit: 0" in msg:
                    self._summary_error.emit(
                        "Daily API quota reached (free tier limit).\n\n"
                        "Options:\n"
                        "• Wait until tomorrow — quota resets daily\n"
                        "• Enable billing at aistudio.google.com/apikey for 1,500 requests/day free"
                    )
                else:
                    delay_match = re.search(r"retry in (\d+\.?\d*)s", msg, re.IGNORECASE)
                    wait = f" Try again in {int(float(delay_match.group(1)))} seconds." if delay_match else ""
                    self._summary_error.emit(f"API rate limit hit.{wait} Please wait and try again.")
            else:
                self._summary_error.emit(f"Error: {msg[:200]}")

    def _on_summary_ready(self, text: str):
        """Handle successful summary generation (runs on UI thread)."""
        self._is_generating_summary = False
        self.summary_btn.setEnabled(True)
        self.summary_btn.setText("📋 Generate Summary")
        self.summary_text.setPlainText(text)

    def _on_summary_error(self, error_msg: str):
        """Handle summary generation error (runs on UI thread)."""
        self._is_generating_summary = False
        self.summary_btn.setEnabled(True)
        self.summary_btn.setText("📋 Generate Summary")
        self.summary_text.setPlainText(f"⚠️ {error_msg}")

    # ── Enrich key events ────────────────────────────────────────────────

    def _on_enrich_key_events(self):
        """Handle Enrich Key Events button — runs batch enrichment."""
        if self._is_enriching:
            return

        if not self._all_race_events:
            return

        settings = get_settings()
        api_key = settings.groq_api_key
        if not api_key:
            self.summary_text.setPlainText(
                "⚠️ No Groq API key configured. Go to Settings to add your key."
            )
            self.summary_panel.setVisible(True)
            return

        self._is_enriching = True
        self.enrich_btn.setEnabled(False)
        self.enrich_btn.setText("⏳ Enriching…")
        self.summary_btn.setEnabled(False)
        self.enrich_progress.setVisible(True)
        self.enrich_progress.setValue(0)

        events_copy = self._all_race_events
        frames_sample = []
        if self._all_frames:
            frames_sample.append(self._all_frames[0])
            for i in range(50, len(self._all_frames), 50):
                frames_sample.append(self._all_frames[i])
            frames_sample.append(self._all_frames[-1])

        thread = threading.Thread(
            target=self._enrich_thread,
            args=(events_copy, frames_sample, api_key, settings.ai_model),
            daemon=True,
        )
        thread.start()

    def _enrich_thread(self, events, frames, api_key, model):
        """Background thread: run batch enrichment and emit progress signals."""
        try:
            from src.insights.ai_commentary_engine import AICommentaryEngine
            engine = AICommentaryEngine(
                api_key=api_key,
                model=model or "llama-3.3-70b-versatile"
            )

            def on_progress(batch_done, total_batches, enriched):
                self._enrich_progress.emit(batch_done, total_batches, enriched)

            stats = engine.enrich_key_events(events, frames,
                                             progress_callback=on_progress)
            self._enrich_done.emit(stats)
        except Exception as e:
            msg = str(e)
            if "429" in msg or "RESOURCE_EXHAUSTED" in msg:
                import re
                if "PerDay" in msg or "limit: 20" in msg or "limit: 0" in msg:
                    self._enrich_error.emit(
                        "Daily API quota reached. Wait until tomorrow or enable billing."
                    )
                else:
                    delay_match = re.search(r"retry in (\d+\.?\d*)s", msg, re.IGNORECASE)
                    wait = f" Try again in {int(float(delay_match.group(1)))}s." if delay_match else ""
                    self._enrich_error.emit(f"Rate limit hit.{wait}")
            else:
                self._enrich_error.emit(f"Error: {msg[:150]}")

    def _on_enrich_progress(self, batch_done: int, total_batches: int, enriched: int):
        """Update progress bar during enrichment."""
        pct = int((batch_done / max(total_batches, 1)) * 100)
        self.enrich_progress.setMaximum(100)
        self.enrich_progress.setValue(pct)
        self.enrich_btn.setText(f"⏳ Batch {batch_done}/{total_batches}…")

    def _on_enrich_done(self, stats: dict):
        """Handle enrichment completion — refresh event cards."""
        self._is_enriching = False
        self.enrich_btn.setEnabled(True)
        self.enrich_btn.setText("✨ Enrich Key Events")
        self.summary_btn.setEnabled(True)
        self.enrich_progress.setVisible(False)

        # Refresh all event cards to show updated ai_commentary text
        for card in self._event_widgets:
            card.refresh_commentary()

        n = stats.get("enriched", 0)
        calls = stats.get("api_calls", 0)
        skipped = stats.get("skipped", 0)
        print(f"Enrichment done: {n} events enriched in {calls} API calls, "
              f"{skipped} low-priority events kept template commentary")

    def _on_enrich_error(self, error_msg: str):
        """Handle enrichment error."""
        self._is_enriching = False
        self.enrich_btn.setEnabled(True)
        self.enrich_btn.setText("✨ Enrich Key Events")
        self.summary_btn.setEnabled(True)
        self.enrich_progress.setVisible(False)
        self.summary_text.setPlainText(f"⚠️ Enrichment failed: {error_msg}")
        self.summary_panel.setVisible(True)


