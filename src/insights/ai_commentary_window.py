def _fmt_race_time(seconds: float) -> str:
    """Format session seconds as H:MM:SS."""
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    return f"{h}:{m:02}:{s:02}"


class EventCard(QFrame):
    """A single event card in the commentary feed."""

    def __init__(self, event: dict, parent=None):
        super().__init__(parent)
        self._event = event          # keep reference for refresh
        self.setObjectName("eventCard")

        style = EVENT_STYLES.get(event.get("type", ""), DEFAULT_STYLE)
        accent = style["accent"]
        bg = style["bg"]

        self.setStyleSheet(f"""
            #eventCard {{
                background: {bg};
                border-left: 3px solid {accent};
                border-radius: 6px;
                padding: 8px 12px;
                margin: 2px 0px;
            }}
            #eventCard:hover {{
                background: rgba(255, 255, 255, 0.06);
            }}
        """)

        layout = QHBoxLayout(self)
        layout.setContentsMargins(8, 6, 8, 6)
        layout.setSpacing(10)

        # Icon
        icon_label = QLabel(style["icon"])
        icon_label.setFont(QFont("Segoe UI Emoji", 18))
        icon_label.setFixedWidth(30)
        icon_label.setAlignment(Qt.AlignCenter)
        layout.addWidget(icon_label)

        # Content column
        content = QVBoxLayout()
        content.setSpacing(2)

        # Header row: type badge + time
        header = QHBoxLayout()
        badge = QLabel(style["label"])
        badge.setFont(QFont("Inter", 9, QFont.Bold))
        badge.setStyleSheet(f"""
            color: {accent};
            background: rgba(255, 255, 255, 0.06);
            border-radius: 3px;
            padding: 1px 6px;
        """)
        header.addWidget(badge)

        lap = event.get("lap", "")
        time_str = _fmt_race_time(event.get("time", 0))
        meta = QLabel(f"Lap {lap}  ·  {time_str}")
        meta.setFont(QFont("Inter", 9))
        meta.setStyleSheet("color: rgba(255, 255, 255, 0.5);")
        header.addWidget(meta)
        header.addStretch()
        content.addLayout(header)

        # Commentary text — prefer AI commentary, fall back to template
        ai_text = event.get("ai_commentary")
        template_text = event.get("commentary", "")
        is_ai = ai_text is not None
        commentary_text = ai_text if is_ai else template_text

        self._commentary_label = QLabel(commentary_text)
        self._commentary_label.setFont(QFont("Inter", 12))
        self._commentary_label.setStyleSheet("color: rgba(255, 255, 255, 0.92);")
        self._commentary_label.setWordWrap(True)
        content.addWidget(self._commentary_label)

        # AI badge if commentary was generated by the LLM
        self._content_layout = content
        self._ai_badge_added = is_ai
        if is_ai:
            self._add_ai_badge(content)

        layout.addLayout(content, 1)

        self.setSizePolicy(QSizePolicy.Expanding, QSizePolicy.Minimum)

    def _add_ai_badge(self, content):
        ai_badge = QLabel("🤖 AI")
        ai_badge.setFont(QFont("Inter", 8, QFont.Bold))
        ai_badge.setStyleSheet("""
            color: #ab47bc;
            background: rgba(171, 71, 188, 0.12);
            border-radius: 3px;
            padding: 1px 5px;
        """)
        ai_badge.setFixedWidth(42)
        content.addWidget(ai_badge)

    def refresh_commentary(self):
        """Update the commentary label in-place after AI enrichment."""
        ai_text = self._event.get("ai_commentary")
        if ai_text and ai_text != self._commentary_label.text():
            self._commentary_label.setText(ai_text)
            if not self._ai_badge_added:
                self._add_ai_badge(self._content_layout)
                self._ai_badge_added = True


